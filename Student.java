package RestController;

public class Student {
	
int sid;
String sname;
String dept;
	
Student()
{
		
}

public int getSid() {
	return sid;
}

public void setSid(int sid) {
	this.sid = sid;
}

public String getSname() {
	return sname;
}

public void setSname(String sname) {
	this.sname = sname;
}

public String getDept() {
	return dept;
}


public void setDept(String dept) {
	this.dept = dept;
}


}
